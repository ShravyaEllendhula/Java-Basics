package com.java.Datatypes;

public class Literals 
{
	public static void main(String[] args) 
	{
		//Integral literals
		byte b = 100;
		short s = 200;
		int num = 13313131;
		long l = 928389283L;
		//float literals
		double num1 = 22.4;
		float num2 = 22.4f;
		//char and string literal
		char ch = 'Z';
		String str = "BeginnersBook";
	}
}
