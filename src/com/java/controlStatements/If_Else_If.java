package com.java.controlStatements;

public class If_Else_If 
{
	public static void main(String[] args) 
	{
		int i=12;
		 
		
		if(i>0)
		{
			System.out.println("i is positive");
		}
		else if(i<0)
		{
			System.out.println("i is negative");
		}
		else
		{
			System.out.println("i is zero");
		}
	}
}
